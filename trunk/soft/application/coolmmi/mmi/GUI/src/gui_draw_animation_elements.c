				 temp_fish_circumvolve_angle =abs(atan2Func( display_fish[element_fish_index].element_fish_y - display_fish[element_fish_index].element_fish_destination_y ,
		   	display_fish[element_fish_index].element_fish_x  - display_fish[element_fish_index].element_fish_destination_x ) - display_fish[element_fish_index].element_fish_circumvolve_angle);
