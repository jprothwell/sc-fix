{
	S32 f_x, f_y;

       f_x =( (S32)(in_costable[angle] *( icon_point_x-icon_offset_x) + in_sintable[angle] *( icon_point_y-icon_offset_y)))>>14;
	f_y =((S32)( -in_sintable[angle] * ( icon_point_x-icon_offset_x) + in_costable[angle] * (icon_point_y-icon_offset_y)))>>14;
      gui_fill_alpha_color(icon_display_x+f_x,icon_display_y+f_y,icon_display_x+f_x+1,icon_display_y+f_y+1,c);
}

