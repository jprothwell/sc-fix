 short atan2Func(int x, int y)
{
int i = 0;
if((x > 0)&&(y> 0))
{
	if(x > y)
	{
		 i =(int)(y*100/x);
		 return(in_tantable[i]);
	}
	else
	{
		i =(int)(x*100/y);
		 return (90 -in_tantable[i]);
	}
}
else if((x < 0)&&(y> 0))
{
	if((-x) > y)
	{
		 i =(int)(y*100/(-x));
		 return (180-in_tantable[i]);
	}
	else
	{
		i =(int)((-x)*100/y);
		 return (in_tantable[i]+90);
	}
}
else if((x < 0)&&(y< 0))
{
	if((-x) > (-y))
	{
		 i =(int)(y*100/x);
		 return (in_tantable[i]+180);
	}
	else
	{
		i =(int)(x*100/y);
		 return (270 - in_tantable[i]);
	}
}
else if((x > 0)&&(y< 0))
{
if((x) > (-y))
	{
		 i =(int)((-y)*100/x);
		 return (360 - in_tantable[i]);
	}
	else
	{
		i =(int)(x*100/(-y));
		 return (in_tantable[i]+270);
	}
}
else if((x == 0)&&(y> 0))
{
     return 90;
}
else if((x == 0)&&(y < 0))
{
     return 270;
}
else if((x > 0)&&(y== 0))
{
     return 0;
}
else if((x < 0)&&(y== 0))
{
     return 180;
}
else if((x == 0)&&(y== 0))
{
     return rand()%360;
}
}

 element_icon_color_offset_struct gui_fill_alpha_color_offset;
void gui_set_fill_alpha_color_offset(S16 R, S16 G, S16 B)
{
	gui_fill_alpha_color_offset.element_icon_color_R_offset = R;
	gui_fill_alpha_color_offset.element_icon_color_G_offset = G;
	gui_fill_alpha_color_offset.element_icon_color_B_offset = B;
}

  void gui_fill_alpha_color(S32 x1, S32 y1, S32 x2, S32 y2, color_t c)
{
    /*----------------------------------------------------------------*/
    /* Local Variables                                                */
    /*----------------------------------------------------------------*/
    U32 alpha = ((U32) c.alpha);
	
    /*----------------------------------------------------------------*/
    /* Code Body                                                      */
    /*----------------------------------------------------------------*/
	
        gdi_effect_alpha_blending_rect(dm_animation_display_handle, x1, y1, x2,y2,alpha, c.r+gui_fill_alpha_color_offset.element_icon_color_R_offset, 
			c.g+gui_fill_alpha_color_offset.element_icon_color_G_offset, c.b+gui_fill_alpha_color_offset.element_icon_color_B_offset );
}

