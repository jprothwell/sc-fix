/*****************************************************************************
 * FUNCTION
 *  get_bengali_single_line_multitap_string
 * DESCRIPTION
 *  
 * PARAMETERS
 *  i       [IN]        
 * RETURNS
 *  
 *****************************************************************************/
UI_character_type *get_bengali_single_line_multitap_string(S32 i)
{
    /*----------------------------------------------------------------*/
    /* Local Variables                                                */
    /*----------------------------------------------------------------*/
    UI_character_type previous_char;

    /*----------------------------------------------------------------*/
    /* Code Body                                                      */
    /*----------------------------------------------------------------*/
    previous_char = gui_single_line_current_character(&MMI_singleline_inputbox);
    if (previous_char)
    {
         
        if ((previous_char >= BENGALI_C_KA && previous_char <= BENGALI_C_HA) ||
            (previous_char >= BENGALI_C_RRA && previous_char <= BENGALI_C_YYA))
             
        {
            return ((UI_character_type*) default_bengalimatra_multitap_strings[i]);
        }
        else
        {
            switch (previous_char)
            {
                     
                case (BENGALI_IV_A):
                     
                case (BENGALI_IV_AA):
                case (BENGALI_IV_I):
                case (BENGALI_IV_II):
                case (BENGALI_IV_U):
                case (BENGALI_IV_UU):
                case (BENGALI_IV_VR):
                case (BENGALI_IV_VL):
                     
                case (BENGALI_IV_E):
                     
                case (BENGALI_IV_AI):
                case (BENGALI_IV_O):
                case (BENGALI_IV_AU):
                     
                    /* CODE REMOVED */
                     
                case (BENGALI_DV_AA):
                case (BENGALI_DV_I):
                case (BENGALI_DV_II):
                case (BENGALI_DV_U):
                case (BENGALI_DV_UU):
                case (BENGALI_DV_VR):
                case (BENGALI_DV_VRR):
                case (BENGALI_DV_E):
                case (BENGALI_DV_AI):
                case (BENGALI_DV_O):
                case (BENGALI_DV_AU):
                {
                     
                    return ((UI_character_type*) default_bengalimatra1_multitap_strings[i]);
                     
                    break;
                }
                default:
                {
                    return ((UI_character_type*) default_bengali_multitap_strings[i]);
                }

            }
        }
    }
    else
    {
        return ((UI_character_type*) default_bengali_multitap_strings[i]);
    }

}
